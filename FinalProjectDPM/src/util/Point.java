package util;

/**
 * A simple class to store points.
 * 
 * @author Andrei Purcarus
 *
 */
public class Point {
	public double x, y;
	
	/**
	 * Default constructor.
	 * @param x The x coordinate in cm.
	 * @param y The y coordinate in cm.
	 */
	public Point(double x, double y) {
		this.x = x;
		this.y = y;
	}
}
